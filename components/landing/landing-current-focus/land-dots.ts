/** Decodes the packed lattice into a flat Float32Array of [lat, lon] pairs in degrees. */
export function decodeLandDots(): Float32Array {
  const bin = atob(LAND_DOTS_B64);
  const bytes = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
  const packed = new Int16Array(bytes.buffer);
  const out = new Float32Array(packed.length);
  for (let i = 0; i < packed.length; i++) out[i] = packed[i] / 100;
  return out;
}
